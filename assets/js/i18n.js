// i18n System Core
// ==========================

const i18n = {
  currentLang: 'es',
  
  init() {
    // 1. Detect saved language or browser language
    const savedLang = localStorage.getItem('pakpaxtor_lang');
    const browserLang = navigator.language.substring(0, 2);
    
    if (savedLang) {
      this.currentLang = savedLang;
    } else if (browserLang === 'en') {
      this.currentLang = 'en';
    }
    
    // 2. Apply translations
    this.translate();
    
    // 3. Setup language switcher
    this.setupSwitcher();
    
    // 4. Update HTML lang attribute
    document.documentElement.lang = this.currentLang;
  },
  
  translate() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translation = translations[this.currentLang][key];
      if (translation) {
        el.innerHTML = translation;
      }
    });
    
    // Update meta tags
    this.updateMetaTags();
    
    // Update page title
    this.updatePageTitle();
  },
  
  updateMetaTags() {
    const pageName = document.body.getAttribute('data-page');
    if (!pageName) return;
    
    const titleKey = `meta.${pageName}.title`;
    const descKey = `meta.${pageName}.desc`;
    
    const title = translations[this.currentLang][titleKey];
    const desc = translations[this.currentLang][descKey];
    
    if (title) {
      const ogTitle = document.querySelector('meta[property="og:title"]');
      if (ogTitle) ogTitle.content = title;
    }
    if (desc) {
      const metaDesc = document.querySelector('meta[name="description"]');
      const ogDesc = document.querySelector('meta[property="og:description"]');
      if (metaDesc) metaDesc.content = desc;
      if (ogDesc) ogDesc.content = desc;
    }
  },
  
  updatePageTitle() {
    const pageName = document.body.getAttribute('data-page');
    if (!pageName) return;
    
    const titleKey = `meta.${pageName}.title`;
    const title = translations[this.currentLang][titleKey];
    if (title) {
      document.title = title;
    }
  },
  
  setupSwitcher() {
    const switcher = document.getElementById('lang-switcher');
    if (!switcher) return;
    
    const btns = switcher.querySelectorAll('button');
    btns.forEach(btn => {
      const lang = btn.getAttribute('data-lang');
      
      // Highlight active language
      if (lang === this.currentLang) {
        btn.classList.add('active');
      } else {
        btn.classList.remove('active');
      }
      
      // Add click handler
      btn.addEventListener('click', () => {
        this.switchLanguage(lang);
      });
    });
  },
  
  switchLanguage(lang) {
    if (lang === this.currentLang) return;
    
    this.currentLang = lang;
    localStorage.setItem('pakpaxtor_lang', lang);
    
    this.translate();
    this.setupSwitcher();
    
    document.documentElement.lang = lang;
  }
};

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => i18n.init());
} else {
  i18n.init();
}
